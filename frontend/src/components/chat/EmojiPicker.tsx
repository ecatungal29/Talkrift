interface Props {
	onEmojiSelect: (emoji: string) => void;
	isOpen: boolean;
	onClose: () => void;
}

export function EmojiPicker({ onEmojiSelect, isOpen, onClose }: Props) {
	const [activeCategory, setActiveCategory] = useState("Smileys");

	if (!isOpen) return null;

	return (
		<TooltipProvider>
			<div className="absolute bottom-20 left-0 bg-background border border-border rounded-lg shadow-lg p-2 z-50 w-80">
				<div className="flex justify-between items-center mb-2">
					<div className="flex gap-1 border-b border-border pb-2">
						{Object.keys(EMOJI_CATEGORIES).map((category) => (
							<Button
								key={category}
								variant={activeCategory === category ? "default" : "ghost"}
								size="sm"
								onClick={() => setActiveCategory(category)}
								className="text-xs px-2 py-1 h-auto">
								{category}
							</Button>
						))}
					</div>
					<Button
						variant="ghost"
						size="sm"
						onClick={onClose}
						className="h-8 w-8 p-0 hover:bg-accent">
						{/* <X className="h-4 w-4" /> */}
					</Button>
				</div>

				<ScrollArea className="h-48">
					<div className="grid grid-cols-8 gap-1">
						{EMOJI_CATEGORIES[
							activeCategory as keyof typeof EMOJI_CATEGORIES
						].map((emoji) => (
							<Tooltip key={emoji}>
								<TooltipTrigger asChild>
									<Button
										variant="ghost"
										size="sm"
										onClick={() => {
											onEmojiSelect(emoji);
										}}
										className="h-8 w-8 p-0 text-lg hover:bg-accent">
										{emoji}
									</Button>
								</TooltipTrigger>
								<TooltipContent>
									<p className="text-xs">{emoji}</p>
								</TooltipContent>
							</Tooltip>
						))}
					</div>
				</ScrollArea>
			</div>
		</TooltipProvider>
	);
}
